package it.assist.jrecordbind.util;

public class TrimmerException extends RuntimeException {

  public TrimmerException() {
  }

  public TrimmerException(String message) {
    super(message);
  }

  public TrimmerException(String message, Throwable cause) {
    super(message, cause);
  }

  public TrimmerException(Throwable cause) {
    super(cause);
  }

}
